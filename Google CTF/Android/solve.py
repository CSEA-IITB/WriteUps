import gmpy2

m = 4294967296
l = [40999019, 2789358025, 656272715, 18374979, 3237618335, 1762529471,
     685548119, 382114257, 1436905469, 2126016673, 3318315423, 797150821]

o = [gmpy2.invert(i, m) for i in l]

s = ''
for i in o:
    t = ''
    for j in range(4):
        t += chr(i % 256)
        i //= 256
    s += t

print(s)
